package jt.oopnext.users;

public class Address {
    private int npa;
    private String street;
    private int number;
    private String city;

    public Address(int npa, String street, int number, String city) {
        this.npa = npa;
        this.street = street;
        this.number = number;
        this.city = city;
    }

    @Override
    public String toString() {
        return "Address{" +
                "npa=" + npa +
                ", street='" + street + '\'' +
                ", number=" + number +
                ", city='" + city + '\'' +
                '}';
    }
}
